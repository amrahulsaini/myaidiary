import { GoogleGenAI } from '@google/genai';
import Database from 'better-sqlite3';
import crypto from 'node:crypto';
import fs from 'node:fs';
const env = fs.readFileSync('.env', 'utf8');
const apiKey = (env.match(/^GEMINI_API_KEY=(.*)$/m) || [])[1].trim();
const mk = ((env.match(/^MASTER_KEY=(.*)$/m) || [])[1] || '').trim();
const MASTER = crypto.createHash('sha256').update(mk).digest();
const tkey = (id) => crypto.createHmac('sha256', MASTER).update('tenant:' + id).digest();
const dec = (b, k) => { if (!b || !b.startsWith('v1:')) return b; try { const raw = Buffer.from(b.slice(3), 'base64'); const d = crypto.createDecipheriv('aes-256-gcm', k, raw.subarray(0, 12)); d.setAuthTag(raw.subarray(12, 28)); return Buffer.concat([d.update(raw.subarray(28)), d.final()]).toString('utf8'); } catch { return ''; } };
const pdb = Database('data/platform.db');
const t = pdb.prepare("SELECT id FROM tenants WHERE email='rahul@loopwar.dev'").get();
const k = tkey(t.id);
const db = Database('data/tenants/' + t.id + '.db');
const persona = dec(db.prepare("SELECT value FROM settings WHERE key='persona'").get().value, k);
const grp = db.prepare("SELECT jid,saved_name FROM contacts WHERE is_group=1 LIMIT 1").get();
const msgs = db.prepare('SELECT direction,body FROM messages WHERE jid=? ORDER BY id DESC LIMIT 30').all(grp.jid).reverse();
const transcript = msgs.map((m) => (m.direction === 'in' ? dec(m.body, k) : 'Me/Zoop: ' + dec(m.body, k))).join('\n');
console.log('group:', dec(grp.saved_name, k), '| transcript chars:', transcript.length, '| persona chars:', persona.length);
const sys = [persona, `\nYou are writing a message to send into the group "${dec(grp.saved_name, k)}".`, `Recent messages there:\n${transcript || '(none)'}`, `\nRahul's instruction to you: write some roasting lines for himanshu`, `\nWrite ONLY the message text — short, punchy, savage but clean (no slurs/vulgar).`].join('\n');
const ai = new GoogleGenAI({ apiKey });
const SAFETY = ['HARASSMENT', 'HATE_SPEECH', 'SEXUALLY_EXPLICIT', 'DANGEROUS_CONTENT', 'CIVIC_INTEGRITY'].map((c) => ({ category: 'HARM_CATEGORY_' + c, threshold: 'BLOCK_NONE' }));
const res = await ai.models.generateContent({ model: 'gemini-2.5-flash', contents: 'write some roasting lines for himanshu', config: { systemInstruction: sys, temperature: 0.95, maxOutputTokens: 300, thinkingConfig: { thinkingBudget: 0 }, safetySettings: SAFETY } });
console.log('TEXT:', JSON.stringify(res.text));
console.log('finish:', res.candidates?.[0]?.finishReason, '| ratings:', JSON.stringify(res.candidates?.[0]?.safetyRatings || []));
console.log('usage:', JSON.stringify(res.usageMetadata || {}));
